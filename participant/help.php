		<h1>Hjälp</h1>
<p>
En <b>deltagare</b> är en fysisk person som tänkt åka på ett lajv. Här fyller du i personuppgifter kopplat till dig själv. Du kan även registrera flera deltagare på samma inloggningskonto. Ett typiskt exempel är en förälder som även registrerar sina barn.
</p>
<p>

En <b>gruppansvarig</b> är en deltagare som fungerar som kontaktperson åt en lajv-grupp (ex en familj, en liga med banditer, ett rallarlag). I de flesta fall är det så att en gruppledare även fungerar som ledare “IN-lajv” för gruppen, men det behöver inte vara så. Gruppledaren är den som sköter kontakten med arrangörsteamet och som tar emot information/intriger som inte alla i gruppen bör känna till.
</p>
<p>
En <b>karaktär</b> är en roll på lajvet. Varje deltagare kan ha flera karaktärer. T ex kan man välja att spela olika karaktärer på olika lajv. Man kan också ha backup-karaktärer eller dubbel-karaktärer. 
</p>
<p>
För att få åka på lajvet behöver du anmäla (och betala för) alla deltagare som ska åka på lajvet. Vid anmälan av en deltagare väljer du vilken eller vilka karaktärer du (och andra deltagare du anmäler) vill spela under det aktuella lajvet.
</p>
<p>
Det går att hantera flera deltagare från ett konto, tex om ni är en familj.
</p>
